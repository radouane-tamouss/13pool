#include <unistd.h>

void	ft_putstr(char *c)
{
	int	i;

	i = 0;
	while (c[i])
	{
		write(1, &c[i], 1);
		i++;
	}
}

int	main(int argc, char **argv)
{
	ft_putstr (argv[argc - argc]);
	write(1, "\n", 1);
	return (0);
}
